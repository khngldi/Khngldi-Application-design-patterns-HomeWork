public class DVDPlayer {
    public void play() {
        System.out.println("DVD-проигрыватель начал воспроизведение");
    }

    public void pause() {
        System.out.println("DVD-проигрыватель на паузе");
    }

    public void stop() {
        System.out.println("DVD-проигрыватель остановлен");
    }
}